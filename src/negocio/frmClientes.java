    private void cmbDepartamentosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbDepartamentosItemStateChanged
        int posicion;
        posicion = cmbDepartamentos.getSelectedIndex();
        lblDepartamentoSeleccionado.setText(String.valueOf(departamentos.getElementAt(posicion)));
        
        //Llenando el cmbMunicipios mediante un modelo
        municipios.removeAllElements();
        nombresMunicipios.removeAllElements();
        try{
            rstMunicipios = municipio.llenarMunicipios(Integer.parseInt(lblDepartamentoSeleccionado.getText()));
            while (rstMunicipios.next()) {
                municipios.addElement(rstMunicipios.getString(2));
                nombresMunicipios.addElement(rstMunicipios.getString(3));
            }
        cmbMunicipios.setModel(nombresMunicipios);
        lblMunicipioSeleccionado.setText(String.valueOf(municipios.getElementAt(0)));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Error", 0);
        }


        
    }//GEN-LAST:event_cmbDepartamentosItemStateChanged

